package com.example.myapplication

import androidx.lifecycle.ViewModel
import kotlinx.coroutines.flow.MutableStateFlow
import kotlinx.coroutines.flow.StateFlow
import java.text.SimpleDateFormat
import java.util.Locale

data class DocumentsUiState(
    val documents: List<Document> = emptyList(),
    val searchQuery: String = "",
    val selectedType: DocumentType = DocumentType.ALL,
    val selectedDateFilter: DateFilter = DateFilter.DEFAULT,
    val isFavoritesMode: Boolean = false
) {
    val visibleDocuments: List<Document>
        get() {
            val dateFormat = SimpleDateFormat("dd.MM.yyyy", Locale.getDefault())

            var result = documents

            if (isFavoritesMode) {
                result = result.filter { it.isFavorite }
            }

            if (searchQuery.isNotBlank()) {
                val query = searchQuery.trim().lowercase()

                result = result.filter { document ->
                    document.title.lowercase().contains(query) ||
                            document.type.displayName.lowercase().contains(query) ||
                            document.content.lowercase().contains(query)
                }
            }

            if (selectedType != DocumentType.ALL) {
                result = result.filter { it.type == selectedType }
            }

            result = when (selectedDateFilter) {
                DateFilter.DEFAULT -> result

                DateFilter.NEW_FIRST -> result.sortedByDescending {
                    runCatching { dateFormat.parse(it.date) }.getOrNull()
                }

                DateFilter.OLD_FIRST -> result.sortedBy {
                    runCatching { dateFormat.parse(it.date) }.getOrNull()
                }
            }

            return result
        }
}

