    private val _state = MutableStateFlow(
        DocumentsUiState(
            documents = initialDocuments
        )
    )

    val state: StateFlow<DocumentsUiState> = _state

    fun onSearchQueryChanged(query: String) {
        _state.value = _state.value.copy(searchQuery = query)
    }

    fun onDocumentTypeSelected(type: DocumentType) {
        _state.value = _state.value.copy(selectedType = type)
    }

    fun onDateFilterSelected(filter: DateFilter) {
        _state.value = _state.value.copy(selectedDateFilter = filter)
    }

    fun resetFilters() {
        _state.value = _state.value.copy(
            selectedType = DocumentType.ALL,
            selectedDateFilter = DateFilter.DEFAULT
        )
    }

    fun toggleFavoritesMode() {
        _state.value = _state.value.copy(
            isFavoritesMode = !_state.value.isFavoritesMode
        )
    }

    fun toggleFavorite(document: Document) {
        val updatedDocuments = _state.value.documents.map {
            if (it.id == document.id) {
                it.copy(isFavorite = !it.isFavorite)
            } else {
                it
            }
        }

        _state.value = _state.value.copy(documents = updatedDocuments)
    }
